#include<stdio.h>
int main(){
    int a=4;
    //switch case is used to check the multiple  condition for a single variable.
    switch(a){
        case 1:
        printf("the value of a is 1");
        break;
        case 2:
        printf("the value of a is 2");
        break;
        case 3:
        printf("the value of a is 3");
        break;
        default:
        printf("the value  of a is greater than 3");
        
    }
}